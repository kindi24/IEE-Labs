
package gentest;

public class Foititis {
    
    private String onoma;
    private int AM;
    
    public Foititis(String onoma, int AM) {
        this. onoma = onoma;
        this.AM = AM;
    }
    
    public int getAM() {
        return AM;
    }
    
    public String toString() {
        return ("Stoixeia foititi: " + onoma + "  " + AM);
    }
    
}
